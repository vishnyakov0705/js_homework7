const regex = /^(?!.*[Aa]).{6,}$/;

// Приклади рядків для тестування
const testStrings = [
  "qwertyui",     // Підходить
  "123456",     // Підходить
  "ABC123",     // містить A
  "івапрол",    // містить a кірилиця
  "123456789a",     // містить a
  "ФІВАПРОЛД"   // містить А кірилиця
];

testStrings.forEach(str => {
  console.log(`"${str}" підходить: ${regex.test(str)}`);
});